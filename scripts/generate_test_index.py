"""Generate a FAISS-compatible test index for the semantic search service.

Two modes are supported via ``--backend``:

``random`` (default)
    Unit-normalised random vectors (seeded, reproducible).  No AWS calls.
    Use for infrastructure / filter / API wiring validation only.  Scores
    will be meaningless because the vectors have no relationship to the text.

``bedrock``
    Real embeddings generated by calling AWS Bedrock Titan embed-text-v1.
    Scores will reflect genuine semantic similarity.  Requires AWS credentials
    and Bedrock access in the target region.

Usage::

    # Random index (no AWS) — infrastructure validation only
    uv run python scripts/generate_test_index.py

    # Real Bedrock embeddings — semantic search validation
    uv run python scripts/generate_test_index.py --backend bedrock \
        --region us-east-1 --output ./bedrock_index

    # Build with Bedrock and upload to S3 in one step
    uv run python scripts/generate_test_index.py --backend bedrock \
        --region us-east-1 --upload

    # Custom output directory
    uv run python scripts/generate_test_index.py --output /tmp/my_index

    # Larger random corpus
    uv run python scripts/generate_test_index.py --extra 200
"""

